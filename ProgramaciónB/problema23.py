# Problema 23. Comparaciones

num1 = float (input ("Introduce un número cualquiera: "))
num2 = float (input("Introduce un número cualquiera: "))

print ("Son iguales. ", num1 == num2)
print ("Son diferentes. ", num1 != num2)
print ("El primero es mayor que el segundo. ", num1 > num2)
print ("El segundo es mayor que el primero. ", num1 < num2)
print ("El primero es mayor o igual que el segundo. ", num1 >= num2)
print ("El segundo es mayor o igual que el primero. ", num1 <= num2)
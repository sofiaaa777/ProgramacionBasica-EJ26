# Problema 9: Tipos de datos

nombre = 'Pedro'
x = 200
y = 15.4

type(nombre)
# El tipo de dato es una cadena. Su función es nombrar variables y almacenar texto que el programa puede mostrar al usuario.

type(x)
# El tipo de dato es un entero. Se utiliza para declarar variables que no pueden ser racionales.

type(y)
# El tipo de dato es un flotante. Es útil para variables que pueden ser tanto enteros como racionales.
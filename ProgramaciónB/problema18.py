# Problema 18. Operaciones matemáticas

import math
num1 = int (input("Introduce un número cualquiera: "))
num2 = int (input ("Introduce un número cualquiera: "))
suma = num1 + num2
resta = num1 - num2
mult = num1 * num2
div = num1 / num2
pot1 = num1 ** 2
pot2 = num2 ** 2
raiz = math.sqrt(num1)
mod = num1 % num2
print ("Suma: ", suma)
print ("Resta: ", resta)
print ("Multiplicación: ", mult)
print ("División: ", div)
print ("Potencia del número 1: ", pot1)
print ("Potencia del número 2: ", pot2)
print ("Raiz cuadrada del primero: ", raiz)
print ("Residuo: ", mod)
# Problema 5: Comparación de cadenas

a = 'Pedro'
b = 'pedro'
print (a == b)
print (a = b)

# (==) se usa para comparar los valores de dos variables mientras que (=) se usa para asignarle un valor cualquiera a una variable. Cada cadena asignada a una variable es diferente ya que cada uno de los caracteres tiene un valor distinto dentro del programa. Si el programa no lee exactamente los mismos caracteres, la comparación falla. Es como comparar valores númericos siferentes.